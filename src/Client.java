
public class Client {

		private int id;
		private int demand;
		
		public Client(int id) {
			this.id = id;
		}
		
		public int getDemand() {
			return demand;
		}
		public void setDemand(int demand) {
			this.demand = demand;
		}

		public int getId() {
			return id;
		}

		public void setId(int id) {
			this.id = id;
		}
		
		

}
